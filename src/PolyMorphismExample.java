public class PolyMorphismExample {
  // If you're here, then you're curious about what this is.
  // It's the start of the big polymorphism example for my code.
  // Eclipse however wants me to make a hundred more classes.
  /**
   * A method that will print a message containing a description about food.
   */
  public void showFood() {
    System.out.println("Wow food is tasty! Here's where a description of some would go.");
  }

  /**
   * Prints a message that tells the user to imagine the food in their mind.
   */
  public void imagineFood() {
    System.out.println("Imagine your dinner here.");
  }
}
